const getStringLengths = (strings) => {
  const lengths = strings.map((str) => str.length);
  return lengths;
};

const getEvenNumbers = (mixedNums) => {
  const evens = mixedNums.filter((num) => num % 2 === 0);
  return evens;
};


const getEvenSquares = (nums) => {
  const evenSquares = nums.filter((num) => num % 2 === 0).map((num) => num * num);
  return evenSquares;
};

const getActiveUsersUnder30 = (users) => {
  const activeUnder30 = users.filter((user) => user.isActive && user.age < 30);
  return activeUnder30;
};

const getTotalScore = (users) => {
  const totalScore = users.reduce((sum, user) => sum + user.score, 0);
  return totalScore;
};

const getFirstHighScorer = (users) => {
  const highScorer = users.find((user) => user.score > 90);
  return highScorer;
};


const findUserById3 = (users) => {
  const userIndex = users.findIndex((user) => user.id === 3);
  users.splice(userIndex, 1);
};
